namespace NotepadPlus
{
    /// <summary>
    /// Represents a text file type.
    /// </summary>
    internal enum TextFileType
    {
        /// <summary>
        /// A rich text file.
        /// </summary>
        RichTextFile,

        /// <summary>
        /// A plain text file.
        /// </summary>
        PlainTextFile,

        /// <summary>
        /// A source code file.
        /// </summary>
        SourceCodeFile
    }
}